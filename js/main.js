let heroChatCount = 0;

async function heroChat(question) {
  question = sanitizeInput(question || '');
  if (!question) return;

  const messagesEl = document.getElementById('heroAiMessages');
  const chipsEl   = document.getElementById('heroAiChips');
  const inputEl   = document.getElementById('heroAiInput');

  // メッセージ欄を表示・チップを非表示
  messagesEl.classList.add('has-messages');
  if (chipsEl) chipsEl.style.display = 'none';
  if (inputEl) inputEl.value = '';

  // ユーザーバブル追加
  addHeroBubble(question, 'user');

  // AIローディングバブル
  const loadingBubble = addHeroBubble('考え中…', 'ai');

  try {
    const aiText = await callGemini(question);
    loadingBubble.innerText = aiText;
  } catch (e) {
    loadingBubble.innerText = 'エラーが発生しました。右下のチャットからお試しください。';
    console.error(e);
  }

  messagesEl.scrollTop = messagesEl.scrollHeight;
  heroChatCount++;

  // 2往復したら「もっと詳しく」ボタンを表示
  if (heroChatCount >= 2 && !document.querySelector('.hero-ai-more')) {
    const moreBtn = document.createElement('button');
    moreBtn.className = 'hero-ai-more';
    moreBtn.innerHTML = 'もっと詳しく話す <i class="ti ti-arrow-right" style="font-size:12px;"></i>';
    moreBtn.onclick = () => {
      document.getElementById('chatWidget').classList.add('active');
    };
    messagesEl.appendChild(moreBtn);
    messagesEl.scrollTop = messagesEl.scrollHeight;
  }
}

function addHeroBubble(text, type) {
  const messagesEl = document.getElementById('heroAiMessages');
  const bubble = document.createElement('div');
  bubble.className = `hero-ai-bubble hero-ai-bubble--${type}`;
  bubble.innerText = text;
  messagesEl.appendChild(bubble);
  messagesEl.scrollTop = messagesEl.scrollHeight;
  return bubble;
}

function fillChatInput(question) {
  const input = document.getElementById('chatInput');
  input.value = question;
  input.focus();
}

function handleChatKeyPress(e) {
  if (e.key === 'Enter') sendChatMessage();
}

async function sendChatMessage() {
  const input = document.getElementById('chatInput');
  const text = sanitizeInput(input.value);
  if (!text) return;
  
  // Add User Message
  addMessageBubble(text, 'user-msg');
  input.value = '';
  
  // Show Loading
  const loadingId = addMessageBubble('考え中...', 'ai-msg', true);
  
  try {
    const aiText = await callGemini(text);
    
    // Replace loading with real response
    document.getElementById(loadingId).innerText = aiText;
  } catch (error) {
    document.getElementById(loadingId).innerText = 'すみません、通信エラーが発生しました。時間を置いて再度お試しください。';
    console.error(error);
  }
}

function addMessageBubble(text, className, isLoading = false) {
  const body = document.getElementById('chatBody');
  const msgDiv = document.createElement('div');
  msgDiv.className = `chat-msg ${className}`;
  
  const bubble = document.createElement('div');
  bubble.className = 'msg-bubble';
  bubble.innerText = text;
  
  const id = 'msg-' + Date.now();
  if (isLoading) bubble.id = id;
  
  msgDiv.appendChild(bubble);
  body.appendChild(msgDiv);
  body.scrollTop = body.scrollHeight;
  return id;
}
