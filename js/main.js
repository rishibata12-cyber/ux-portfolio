// Scroll animation observer
function initScrollObserver() {
  const observerOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.1
  };

  const observer = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, observerOptions);

  // Apply to all elements with fade-up class that are part of the active page
  document.querySelectorAll('.page.active .fade-up').forEach(el => {
    observer.observe(el);
  });
}

function resetScrollAnimations() {
  // Reset all fade-up elements to invisible before changing pages
  document.querySelectorAll('.fade-up').forEach(el => {
    el.classList.remove('visible');
  });
}

function showPage(name){
  resetScrollAnimations();
  
  // Wait a tiny bit for the opacity transition
  setTimeout(() => {
    document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
    const newPage = document.getElementById('pg-' + name);
    newPage.classList.add('active');
    
    // Add fade-up class to sections if not present
    if (name === 'home') {
      const sections = newPage.querySelectorAll('.hsec, .hero');
      sections.forEach((sec, idx) => {
        sec.classList.add('fade-up');
        if(idx === 0) sec.classList.add('visible'); // Show hero immediately
      });
      const logo = document.getElementById('snav-logo');
      if(logo) logo.style.display = 'none';
    } else {
      const logo = document.getElementById('snav-logo');
      if(logo) logo.style.display = 'block';
    }
    
    window.scrollTo({ top: 0, behavior: 'smooth' });
    initScrollObserver();
  }, 150);
}

